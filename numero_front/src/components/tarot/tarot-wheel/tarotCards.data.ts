/**
 * Получить карты по масти
 */
export const getArcanaBysuit = (suit: 'coins' | 'cups' | 'swords' | 'wands') => {
  switch (suit) {
    case 'coins': return coinsArcana;
    case 'cups': return cupsArcana;
    case 'swords': return swordsArcana;
    case 'wands': return wandsArcana;
  }
};

/**
 * Перемешать колоду
 */
export const shuffleCards = (cards: TarotWheelCard[]): TarotWheelCard[] => {
  const shuffled = [...cards];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
};

